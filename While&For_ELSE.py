"""

While Else & For Else

"""

i = 1

while i <= 5:
    print(i)
    i += 1
else:
    print("Loop Completed !!!")

"""
What else block statement used
"""

i = 1

while i <= 5:
    if i == 5:
        break
    print(i)
    i += 1
else:
    print("Loop Completed !!!")

"""
FOR LOOP
"""

for i in range(1, 21):
    print(i)
else:
    print("For Loop Completed !!!")
